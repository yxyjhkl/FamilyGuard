// 根据分类获取所有问题
export const getHelpByCategory = (categoryId: string): HelpCategory | undefined => {
  return helpContent.find(cat => cat.id === categoryId);
};

// 搜索帮助内容
export const searchHelp = (keyword: string): HelpItem[] => {
  const lowerKeyword = keyword.toLowerCase();
  const results: HelpItem[] = [];
  
  helpContent.forEach(category => {
    category.items.forEach(item => {
      if (
        item.title.toLowerCase().includes(lowerKeyword) ||
        item.content.toLowerCase().includes(lowerKeyword)
      ) {
        results.push(item);
      }
    });
  });
  
  return results;
};
